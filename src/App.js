const generateEmptyGrid = (col, row) => {
  const rows = []
    for(let i=0; i < row; i++){
      rows.push(Array.from(Array(col), () => 0))
    }  
    return rows
}

function App() {
  const numCol = 30
  const numRow = 30
  const [grid, setGrid] = useState(() => {
    return generateEmptyGrid(numCol, numRow)
  })
  const [fast, setFast] = useState(false)
  const [med, setMed] = useState(false)
  const [slow, setSlow] = useState(true)
  const [speed, setSpeed] = useState(100)
  const [red, setRed] = useState(false)
  const [black, setBlack] = useState(true)
  const [orange, setOrange] = useState(false)
  const [yellow, setYellow] = useState(false)
  const [color, setColor] = useState('black')
  const [running, setRunning] = useState(false)
  const [generation, setGeneration] = useState(0)

  useEffect(() => {
    if(red === true){
      setColor('red')
    } else if(black === true) {
      setColor('black')
    } else if(orange === true) {
      setColor('orange')
    } else if(yellow === true) {
      setColor('yellow')
    }
  }, [red, black, orange, yellow])

  useEffect(() => {
    if(fast === true){
      setSpeed(10)
    } else if (med === true){
      setSpeed(100)
    } else if(slow === true) {
      setSpeed(1000)
    }
  }, [fast, med, slow])

  const incrementGeneration = (generation) => {
    setGeneration(generation + 1)
  }

  const runningRef = useRef(running)
  runningRef.current = running

  const changeRed = () => {
    setYellow(false)
    setOrange (false)
    setBlack(false)
    setRed(true)
  }

  const changeBlack = () => {
    setYellow(false)
    setOrange(false)
    setRed(false)
    setBlack(true)
  }

  const changeOrange = () => {
    setYellow(false)
    setRed(false)
    setBlack(false)
    setOrange(true)
  }

  const changeYellow = () => {
    setOrange(false)
    setRed(false)
    setBlack(false)
    setYellow(true)
  }

  const changeFast = () => {
    setMed(false)
    setSlow(false)
    setFast(true)
  }

  const changeMed = () => {
    setSlow(false)
    setFast(false)
    setMed(true)
  }

  const changeSlow = () => {
    setFast(false)
    setMed(false)
    setSlow(true)
  }

  const runSimulation = useCallback(() => {
    // incrementGeneration(generation)
    if (!runningRef.current){
      return
    }
    setGrid(g => {
      // incrementGeneration(generation)
      return produce(g, gridCopy => {
        // incrementGeneration(generation)
        for(let i = 0; i < numRow; i++){
          for(let k = 0; k < numCol; k++){
            let neighbors = 0
            operations.forEach(([x, y]) => {
              const newI = i+x
              const newK = k+y
              if(newI >= 0 && newI < numRow && newK >= 0 && newK < numCol){
                neighbors += g[newI][newK]
              }
            })
            if(neighbors < 2 || neighbors > 3){ 
              gridCopy[i][k] = 0
            } else if (g[i][k] === 0 && neighbors === 3){
              gridCopy[i][k] = 1
            }
          }
        }
      })
    })
    
    // incrementGeneration(generation)
    setTimeout(incrementGeneration(generation), speed)
    setTimeout(runSimulation, speed)
  }, [speed, numCol, numRow, generation])

  // console.log(grid)

  return (
    <>
      {/* entire page */}
      <div className='main'>

        {/* title and grid */}
        <div className='header'>
          <h1>Conway's Game of Life!</h1>

          <div style={{
            display: 'grid',
            gridTemplateColumns: `repeat(${numCol}, 20px)`
            }}
          >
            {grid.map((rows, i) => 
              rows.map((col, k) => (
                <div 
                  className='squares'
                  key={`${i}-${k}`} 
                  onClick={() => {
                    if(running){
                      setRunning(!running)
                    }
                    const newGrid = produce(grid, gridCopy => {
                      gridCopy[i][k] = gridCopy[i][k] ? 0 : 1
                    })
                    setGrid(newGrid)
                  }}
                  style={{
                    backgroundColor: grid[i][k] ? color : undefined
                  }}
                />
              ))
            )}
          </div>

          <div className='nes-container with-title'>
            <p className='title'>Rules</p>
            <div className="lists">
              <ul className="nes-list is-disc">
                <li>Each cell will live or die at the next generation depending on how many neighbors it has currently.</li>
                <li>Live cells with 2 or 3 neighbors will survive into the next generation</li>
                <li>Any dead cell with 3 live neighbors will become a live cell.</li>
                <li>Live cells with only one neighbor or with 4 or more neighbors will die.</li>
                <li>The process will repeat until you click stop!</li>
              </ul>
            </div>
          </div>
        </div>

        <div className='extras'>
          <div className='presets nes-container with-title is-centered'>
            <p className='title'>Presets</p>
            <button 
              className='nes-btn'
              onClick={() => {
                setGrid(presetOne())
              }}
            >
              Preset One
            </button>
            <button 
              className='nes-btn'
              onClick={() => {
                setGrid(presetTwo())
              }}
            >
              Preset Two
            </button>
            <button 
              className='nes-btn'
              onClick={() => {
                setGrid(presetThree())
              }}
            >
              Preset Three
            </button>
          </div>
          
          <div className='colors nes-container with-title'>
            <p className='title'>Preferences</p>
            <div className='columns'>
              <div className='flexes'>
                <button className={red ? 'red colorbut': 'colorbut redout'} onClick={() => changeRed()}/>
                <button className={black ? 'black colorbut' : 'colorbut'} onClick={() => changeBlack()}/>
              </div>
              <div className='flexes'>
                <button className={orange ? 'orange colorbut': 'colorbut orangeout'} onClick={() => changeOrange()}/>
                <button className={yellow ? 'yellow colorbut' : 'colorbut yellowout'} onClick={() => changeYellow()}/>
              </div>
            </div>
            <button className={fast ? 'current speed nes-btn' : 'speed nes-btn'} onClick={() => changeFast()}>Fast</button>
            <button className={med ? 'current speed nes-btn' : 'speed nes-btn'} onClick={() => changeMed()}>Medium</button>
            <button className={slow ? 'current speed nes-btn' : 'speed nes-btn'} onClick={() => changeSlow()}>Slow</button>
          </div>
              
          <div className='buttons nes-container with-title'>
            <p className='title'>Controls</p>
            <button
              className='nes-btn'
              onClick={() => {
                setRunning(!running)
                if(!running){
                  runningRef.current = true
                  runSimulation()
                }
              }}
            >
              {running ? 'Stop' : 'Start'}
            </button>
            <button 
              className='nes-btn'
              onClick={() => {
                if(running){
                  setRunning(!running)
                }
                setGrid(generateEmptyGrid(numCol, numRow))
              }}
            >
              Clear
            </button>
            <button 
              className='nes-btn'
              onClick={() => {
                if(running){
                  setRunning(!running)
                }
                const rows = []
                for(let i=0; i < numRow; i++){
                  rows.push(Array.from(Array(numCol), () => Math.random() > .7 ? 1 : 0))
                }  
                setGrid(rows)
              }}
            >
              Randomize
            </button>
          </div>
          
          <div className='nes-container with-title generationcontain'>
            <p className='title'>Generation</p>
            <span className='generation'>{generation}</span>
          </div>

        </div>
      </div>
    </>
  )
}

export default App;
